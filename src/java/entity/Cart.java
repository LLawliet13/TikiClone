package entity;

/**
 *
 * @author DELL
 */
public class Cart {
    int cid ;
    String pid ;
    int quantity;
    int status;

    public Cart(int cid, String pid, int quantity, int status) {
        this.cid = cid;
        this.pid = pid;
        this.quantity = quantity;
        this.status = status;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }

    

    public int getCid() {
        return cid;
    }

    public void setCid(int cid) {
        this.cid = cid;
    }

    public String getPid() {
        return pid;
    }

    public void setPid(String pid) {
        this.pid = pid;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }
    
    
    
}
